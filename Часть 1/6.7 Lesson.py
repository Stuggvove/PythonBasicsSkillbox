# Задача 6. Вклады

# Вклад в банке составляет X рублей.
# Ежегодно он увеличивается на P процентов,
# после чего дробная часть копеек отбрасывается.

# Определите, через сколько лет вклад составит не менее Y рублей.

# Напишите программу,
# которая по данным числам X, Y, P определяет,
# сколько лет пройдёт, прежде чем сумма достигнет значения Y.

contribution = int(input('Вклад составляет: '))
percent = int(input('На сколько процентов увеличился вклад? '))
summ = int(input('Ожидаемая сумма вклада: '))
year = 0
while summ > contribution:
    contribution += percent * (contribution / 100)
    year += 1
print('Через', year, 'лет', 'вклад составит:', contribution)

